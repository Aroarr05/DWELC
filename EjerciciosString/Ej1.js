let variable1 = "Hello";
let variable2 = "World";

//TRADICIONAL CONCATENATION
console.log("Using traditional String concatenation");
console.log("\'" + variable1 + "\' and \'" + variable2 +"\'");
console.log("\"" + variable1 + "\" and \"" + variable2 +"\"");
console.log("\`" + variable1 + "\` and \`" + variable2 +"\`");

//STRING LITERALS
console.log("\Using String literals");
console.log(`'${variable1}\' and \ '${variable2}'`);
console.log(`"${variable1}\" and \ "${variable2}"`);
console.log(`\`${variable1}\` and \`${variable2}\``);
